package Day5.Encapsulation;

public class Setter {
    private int emp_id;
    private String emp_name;
    private float emp_salary;

    public void setId(int id) {
        this.emp_id = id;
    }

    public void setName(String name) {
        this.emp_name = name;
    }

    public void setSalary(float salary) {
        this.emp_salary = salary;
    }
}
